package lesson3;

public class ComparisonOperators {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;

        boolean c = a>b;
        boolean d = a<=b;
        boolean e = a==b;
        boolean f = a!=b;


        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}