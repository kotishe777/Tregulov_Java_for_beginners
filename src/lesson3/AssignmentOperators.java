package lesson3;

public class AssignmentOperators {
    public static void main(String[] args) {

        int a=3;
        int b=5;
        int c=7;
        int d=9;

        a+=10;
        b-=2;
        c*=4;
        d/=3;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
